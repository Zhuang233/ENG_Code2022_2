/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
  if (__HAL_UART_GET_FLAG(&huart1, UART_FLAG_IDLE) && 
			__HAL_UART_GET_IT_SOURCE(&huart1, UART_IT_IDLE))
	{
    /* clear idle it flag avoid idle interrupt all the time */
    __HAL_UART_CLEAR_IDLEFLAG(&huart1);
    
		/* clear DMA transfer complete flag */
		__HAL_DMA_DISABLE(&hdma_usart1_rx);

		/* handle dbus data dbus_buf from DMA */
		if ((USART1_RX_BUFFER_SIZE - hdma_usart1_rx.Instance->NDTR) == RC_FRAME_LENGTH)
		{
			DBUS_decode(USART1_Rx_Buffer);	
		}
		
		/* restart dma transmission */
		__HAL_DMA_SET_COUNTER(huart1.hdmarx, USART1_RX_BUFFER_SIZE);
		__HAL_DMA_ENABLE(&hdma_usart1_rx);

	}
  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

/**
  * @brief This function handles USART3 global interrupt.
  */
void USART3_IRQHandler(void)
{
  /* USER CODE BEGIN USART3_IRQn 0 */
  if (__HAL_UART_GET_FLAG(&huart3, UART_FLAG_IDLE) && 
			__HAL_UART_GET_IT_SOURCE(&huart3, UART_IT_IDLE))
	{
    /* clear idle it flag avoid idle interrupt all the time */
    __HAL_UART_CLEAR_IDLEFLAG(&huart3);
    
		/* clear DMA transfer complete flag */
		__HAL_DMA_DISABLE(&hdma_usart3_rx);

		/* handle referee data from DMA */
    referee_data_solve(USART3_Rx_Buffer);
		/* restart dma transmission */
		__HAL_DMA_SET_COUNTER(huart3.hdmarx, USART3_RX_BUFFER_SIZE);
		__HAL_DMA_ENABLE(&hdma_usart3_rx);

	}
  /* USER CODE END USART3_IRQn 0 */
  HAL_UART_IRQHandler(&huart3);
  /* USER CODE BEGIN USART3_IRQn 1 */

  /* USER CODE END USART3_IRQn 1 */
}

